package mctees.beans;

public class CategoriaBean {
	
	private String codice;
	private String nome;
	
	public CategoriaBean(){
	}

	public String getCodice() {
		return codice;
	}

	public void setCodice(String codice) {
		this.codice = codice;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
}
